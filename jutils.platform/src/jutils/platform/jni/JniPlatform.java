package jutils.platform.jni;

import java.util.List;

/*******************************************************************************
 * 
 ******************************************************************************/
class JniPlatform
{
    /***************************************************************************
     * 
     **************************************************************************/
    private JniPlatform()
    {
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    native boolean initialize();

    /***************************************************************************
     * @return
     **************************************************************************/
    native boolean destroy();

    /***************************************************************************
     * @param ports list of port names
     * @return
     **************************************************************************/
    native boolean listPorts( List<String> ports );

    /**  */
    private static JniPlatform PLATFORM = null;

    /***************************************************************************
     * @return
     **************************************************************************/
    static JniPlatform getPlatform()
    {
        if( PLATFORM == null )
        {
            PLATFORM = new JniPlatform();
        }

        return PLATFORM;
    }
}
