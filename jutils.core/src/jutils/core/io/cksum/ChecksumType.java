package jutils.core.io.cksum;

import jutils.core.INamedItem;

/*******************************************************************************
 *
 ******************************************************************************/
public enum ChecksumType implements INamedItem
{
    /**  */
    SHA_256( "SHA-256" ),
    /**  */
    MD5( "MD5" ),
    /**  */
    CRC16( "CRC-16" ),
    /**  */
    CRC32( "CRC-32" ),
    /**  */

    /**  */
    SHA_1( "SHA-1" );

    /**  */
    public final String name;

    /***************************************************************************
     * @param name
     **************************************************************************/
    private ChecksumType( String name )
    {
        this.name = name;
    }

    /***************************************************************************
     * {@inheritDoc}
     **************************************************************************/
    @Override
    public String getName()
    {
        return name;
    }
}
