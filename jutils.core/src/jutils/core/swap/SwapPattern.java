package jutils.core.swap;

import jutils.core.INamedValue;

/*******************************************************************************
 * 
 ******************************************************************************/
public enum SwapPattern implements INamedValue
{
    /** |0|1| -> |0|1|. */
    NONE( 0, "None" ),
    /** |0|1| -> |1|0|. WORD16 is the same as ENDIAN16. */
    ENDIAN16( 2, "Endian-16" ),
    /** |0|1|2|3| -> |3|2|1|0|. */
    ENDIAN32( 4, "Endian-32" ),
    /** |0|1|2|3|4|5|6|7| -> |7|6|5|4|3|2|1|0|. */
    ENDIAN64( 8, "Endian-64" ),
    /** |0|1|2|3| -> |2|3|0|1|. */
    WORD16( 4, "Word-16" ),
    /** |0|1|2|3|4|5|6|7| -> |4|5|6|7|0|1|2|3|. */
    WORD32( 8, "Word-32" );

    /**  */
    public final int value;
    /**  */
    public final String name;

    /***************************************************************************
     * @param value
     * @param name
     **************************************************************************/
    private SwapPattern( int value, String name )
    {
        this.value = value;
        this.name = name;
    }

    /***************************************************************************
     * {@inheritDoc}
     **************************************************************************/
    @Override
    public String getName()
    {
        return name;
    }

    /***************************************************************************
     * {@inheritDoc}
     **************************************************************************/
    @Override
    public int getValue()
    {
        return value;
    }
}
