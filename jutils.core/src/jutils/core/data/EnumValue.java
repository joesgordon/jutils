package jutils.core.data;

import jutils.core.INamedValue;

/*******************************************************************************
 * @param <T>
 ******************************************************************************/
public class EnumValue<T extends INamedValue>
{
    /**  */
    private T value;
    /**  */
    private int id;

    /***************************************************************************
     * @param invalidId
     **************************************************************************/
    public EnumValue( int invalidId )
    {
        this( invalidId, null );
    }

    /***************************************************************************
     * @param value
     **************************************************************************/
    public EnumValue( T value )
    {
        this( value.getValue(), value );
    }

    /***************************************************************************
     * @param id
     * @param value
     **************************************************************************/
    public EnumValue( int id, T value )
    {
        this.id = id;
        this.value = value;
    }

    /***************************************************************************
     * @param id
     **************************************************************************/
    public void setId( int id, T value )
    {
        this.id = id;
        this.value = value;
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    public int getId()
    {
        return this.id;
    }

    /***************************************************************************
     * @param value
     **************************************************************************/
    public void setValue( T value )
    {
        this.value = value;
        this.id = value.getValue();
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    public T getValue()
    {
        return value;
    }
}
