package jutils.core.data;

/*******************************************************************************
 * @param <A>
 * @param <B>
 ******************************************************************************/
public class Pair<A, B>
{
    /**  */
    public final A a;
    /**  */
    public final B b;

    /***************************************************************************
     * 
     **************************************************************************/
    public Pair()
    {
        this( null, null );
    }

    /***************************************************************************
     * @param valueA
     * @param valueB
     **************************************************************************/
    public Pair( A valueA, B valueB )
    {
        this.a = valueA;
        this.b = valueB;
    }
}
