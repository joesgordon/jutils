package org.jutils.core.data;

/*******************************************************************************
 * @param <A>
 * @param <B>
 ******************************************************************************/
public class Pair<A, B>
{
    /**  */
    public A a;
    /**  */
    public B b;

    /***************************************************************************
     * 
     **************************************************************************/
    public Pair()
    {
        this( null, null );
    }

    /***************************************************************************
     * @param valueA
     * @param valueB
     **************************************************************************/
    public Pair( A a, B b )
    {
        this.a = a;
        this.b = b;
    }
}
