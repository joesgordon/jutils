package jutils.iris.data;

import java.awt.Color;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IMonoColorModel
{
    /***************************************************************************
     * @param pixel
     * @return
     **************************************************************************/
    public default Color getColor( int pixel )
    {
        return new Color( getColorValue( pixel ) );
    }

    /***************************************************************************
     * @param pixel
     * @return
     **************************************************************************/
    public int getColorValue( int pixel );
}
