package jutils.iris.data;

import java.awt.Color;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IColorModel
{
    /***************************************************************************
     * @param pixel
     * @return
     **************************************************************************/
    public default Color getColor( long pixel )
    {
        return new Color( getColorValue( pixel ) );
    }

    /***************************************************************************
     * @param pixel
     * @return
     **************************************************************************/
    public int getColorValue( long pixel );
}
