package jutils.iris.data;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IRaster
{
    /***************************************************************************
     * @return a copy of the image configuration.
     **************************************************************************/
    public ImageConfig getConfig();

    /***************************************************************************
     * @param p
     * @return
     **************************************************************************/
    public long getPixel( int p );

    /***************************************************************************
     * @param p
     * @param value
     **************************************************************************/
    public void setPixel( int p, long value );

    /***************************************************************************
     * @param x
     * @param y
     * @return
     **************************************************************************/
    public long getPixel( int x, int y );

    /***************************************************************************
     * @param x
     * @param y
     * @param value
     **************************************************************************/
    public void setPixel( int x, int y, long value );

    /***************************************************************************
     * @param p
     * @param b
     * @return
     **************************************************************************/
    public int getBand( int p, int b );

    /***************************************************************************
     * @param n
     * @param b
     * @param value
     **************************************************************************/
    public void setBand( int n, int b, int value );

    /***************************************************************************
     * @param x
     * @param y
     * @param c
     * @return
     **************************************************************************/
    public int getBand( int x, int y, int c );

    /***************************************************************************
     * @param x
     * @param y
     * @param c
     * @param value
     **************************************************************************/
    public void setBand( int x, int y, int c, int value );
}
