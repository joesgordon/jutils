package org.jutils.ui.hex;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IByteBuffer
{
    /***************************************************************************
     * @param index
     * @return
     **************************************************************************/
    public byte get( int index );

    /***************************************************************************
     * @return
     **************************************************************************/
    public byte [] getBytes();

    /***************************************************************************
     * @return
     **************************************************************************/
    public int size();

    /***************************************************************************
     * @return
     **************************************************************************/
    public boolean isEditable();

    /***************************************************************************
     * @param index
     * @param value
     **************************************************************************/
    public void set( int index, byte value );
}
